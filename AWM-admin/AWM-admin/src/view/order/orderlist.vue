<template>
  <div>
    <div class="mtop">
      <el-row>
        <el-button disabled>默认按钮</el-button>
        <el-button type="primary" disabled>主要按钮</el-button>
        <el-button type="success" disabled>成功按钮</el-button>
        <el-button type="info" disabled>信息按钮</el-button>
        <el-button type="warning" disabled>警告按钮</el-button>
        <el-button type="danger" disabled>危险按钮</el-button>
      </el-row>
    </div>
    <div class="mtop">
      <el-row>
        <el-form :inline="true" :model="formInline" class="demo-form-inline">
          <el-form-item label="设备ID">
            <el-input v-model="formInline.user" placeholder="设备ID"></el-input>
          </el-form-item>
          <el-form-item label="品牌">
            <el-select v-model="formInline.region" placeholder="请选择品牌">
              <el-option label="海尔" value="海尔"></el-option>
              <el-option label="美的" value="美的"></el-option>
              <el-option label="其他" value="其他"></el-option>
            </el-select>
          </el-form-item>
          <el-form-item>
            <el-button type="primary" @click="onSubmit">查询</el-button>
          </el-form-item>
        </el-form>
      </el-row>
    </div>
    <el-table :data="info" style="width: 100%">
      <el-table-column prop="createTime" label="订单创建时间"></el-table-column>
      <el-table-column prop="startTime" label="订单开始时间"></el-table-column>
      <el-table-column prop="orderId" label="订单ID"> </el-table-column>
      <el-table-column prop="customerId" label="顾客ID"></el-table-column>
      <el-table-column prop="machineId" label="设备ID"> </el-table-column>
      <el-table-column prop="orderState" width="80" label="订单状态">
      </el-table-column>
      <el-table-column prop="serverlevel" width="80" label="服务">
      </el-table-column>
      <el-table-column>
        <el-button type="primary" icon="el-icon-edit"></el-button>
        <el-button type="primary" icon="el-icon-delete"></el-button>
      </el-table-column>
    </el-table>
    <el-pagination
      background
      :hide-on-single-page="this.hosp"
      layout="prev, pager, next"
      @current-change="handleCurrentChange"
      @prev-click="prev"
      @next-click="next"
      :page-size="this.size"
      :total="this.totals"
    >
    </el-pagination>
  </div>
</template>
<script>
import { formatDate } from "../../utils/formatDate.js";
export default {
  watch: {
    current() {
      this.axios
        .get("/OrderList/" + this.current + "/" + this.size, {
          headers: {
            Authorization: "Bearer " + sessionStorage.getItem("access_token"),
          }, //oauth2.0认证
        })
        .then((response) => (this.info = response.data));
    },
  },
  data() {
    return {
      hosp: true,
      totals: 0,
      current: 1,
      size: 7,
      info: null,
      formInline: {
        user: "",
        region: "",
      },
    };
  },
  created() {
    this.axios
      .get("/OrderList/" + this.current + "/" + this.size, {
        headers: {
          Authorization: "Bearer " + sessionStorage.getItem("access_token"),
        }, //oauth2.0认证
      })
      .then((response) => (this.info = response.data));
    this.axios
      .get("/CountOrder", {
        headers: {
          Authorization: "Bearer " + sessionStorage.getItem("access_token"),
        }, //oauth2.0认证
      })
      .then((response) => (this.totals = response.data));
  },
  filters: {
    formatDate(time) {
      var date = new Date(time);
      return formatDate(date, "yyyy-MM-dd hh:mm");
    },
  },
  methods: {
    prev() {
      this.current -= 1;
    },
    next() {
      this.current += 1;
    },
    // whitchpage() {},
    handleCurrentChange(val) {
      // console.log(`当前页: ${val}`);
      this.current = val;
    },
    onSubmit() {
      console.log("submit!");
    },
  },
};
</script>
